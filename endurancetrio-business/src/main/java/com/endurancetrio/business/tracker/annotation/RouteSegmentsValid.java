package com.endurancetrio.business.tracker.annotation;

import com.endurancetrio.business.tracker.validator.RouteSegmentsValidator;
import jakarta.validation.Constraint;
import jakarta.validation.Payload;
import java.lang.annotation.Documented;
import java.lang.annotation.ElementType;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

@Target(ElementType.TYPE)
@Retention(RetentionPolicy.RUNTIME)
@Constraint(validatedBy = RouteSegmentsValidator.class)
@Documented
public @interface RouteSegmentsValid {

  String message() default "Route segments must form a continuous sequence (endDevice of segment N must match startDevice of segment N+1).";

  Class<?>[] groups() default {};

  Class<? extends Payload>[] payload() default {};
}
